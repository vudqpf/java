package dto;

public class J0729_1_dto {
	String id, name, area;
	int age;
	public J0729_1_dto(String id, String name, String area, int age) {
		super();
		this.id = id;
		this.name = name;
		this.area = area;
		this.age = age;
	}
	public void setId(String id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getArea() {
		return area;
	}
	public int getAge() {
		return age;
	}
	
}
