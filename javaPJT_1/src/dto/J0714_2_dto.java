package dto;

public class J0714_2_dto {
	String name, area;
	int age;
	public J0714_2_dto(String name, String area, int age) {
		super();
		this.name = name;
		this.area = area;
		this.age = age;	
	}
	
	public String getName() {
		return name;
	}
	public String getArea() {
		return area;
	}
	public int getAge() {
		return age;
	}
	
}
